          Warum Wagi-Lauf
        </x-headings.h1>
        <p>Regelmässige Bewegung und sportliche Aktivitäten tragen einen wesentlichen Anteil zur Gesundheit bei. Das ist nichts Neues, aber wusstest du, dass es für Menschen mit Beeinträchtigung nach wie vor schwer ist, Zugang zu öffentlichen Fitnesszentren oder Sportanlagen zu haben?</p>
        <p><strong>Im Artikel 30 der UNO Behindertenrechtskonvention ist festgehalten,</strong> dass Massnahmen für Menschen mit Beeinträchtigung getroffen werden müssen, um die gleichberechtigte Teilnahme an Erholungs-, Freizeit- und Sportaktivitäten zu ermöglichen. Doch oft fehlt nur schon die nötige Begleitung oder der barrierefreie Zugang, die den Besuch überhaupt erst möglich machen.</p>
        <p>Auf diesen Umstand möchten wir aufmerksam machen: <strong>Im Rahmen der Aktionstage der Behindertenrechte der Stadt Uster laden wir ein, am Wagi-Lauf 2024 als Läuferin oder Läufer teilzunehmen.</strong> Der Teilnahme-Beitrag kommt vollumfänglich unserem Spendenprojekt Gesundheit und Fitness zu Gute. Es ermöglichst uns u.a., einen barrierefreien und bedarfsgerechten Fitnessraum im Wagerenhof zu realisieren, der für Menschen mit mehrfacher Beeinträchtigung optimal nutzbar ist.</p>
        <p>Zuschauerinnen und Zuschauer, die tatkräftig anfeuern und mitfeiern sind natürlich auch willkommen.</p>
